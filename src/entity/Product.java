package entity;

public class Product {

	private int productId;
	private int numberOfProductItems;
	private int weight;
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public int getNumberOfProductItems() {
		return numberOfProductItems;
	}
	public void setNumberOfProductItems(int numberOfProductItems) {
		this.numberOfProductItems = numberOfProductItems;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	
}
